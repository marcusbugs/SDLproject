constexpr uint32_t gord_fontW = sys_font_w, gord_fontH = sys_font_h;


[[nodiscard]] SDL_Surface* decodeFontDataToSurface() {
	SDL_Surface* surf =
		SDL_CreateRGBSurfaceWithFormat(
			0,	// flags
			gord_fontW, gord_fontH,	// width and height
			1,
			SDL_PIXELFORMAT_RGBA8888 // depth
	);

	// Static cast our surface to unsigned ints so it's easier to update (usually void*)
	uint32_t* pixels = static_cast<uint32_t*>(surf->pixels);
	const int expected_pixels = surf->w * surf->h;

	// Loop through and fill in relevant pixels.
	int pixel_idx = 0;
	for (int val : fontEncoding) {
		const bool fill = val & 128u;
		const int count = val & 127u;

		for (int i = 0; i < count && pixel_idx < expected_pixels; i++) {
			pixels[pixel_idx++] = fill ? 0xFFFFFFFF : 0x00000000;
		}
	}

	return surf;
}


void gord::StaticFont::initialize(SDL_Renderer* renderer) {
	// initialized ensures we don't accidentally not destroy a texture
	// we aren't guaranteed font_texture is initialized because it is a static member var.
	static bool initialized = false;
	if (initialized && font_texture != nullptr) {
		SDL_DestroyTexture(font_texture);
	}
	initialized = true;

	SDL_Surface* surf = decodeFontDataToSurface();
	font_texture = SDL_CreateTextureFromSurface(renderer, surf);
	SDL_FreeSurface(surf);

	SDL_SetTextureBlendMode(font_texture, SDL_BLENDMODE_BLEND);
	SDL_SetTextureAlphaMod(font_texture, 255);
	SDL_SetTextureColorMod(font_texture, 255, 255, 255);
}

void gord::StaticFont::renderText(SDL_Renderer* renderer, const char text[], const SDL_Point pos, int scale) {
	SDL_Rect dst = { pos.x, pos.y, 0, 0 };
	const size_t len = strlen(text);

	// First iterate through all of our characters.
	for (size_t i = 0; i < len; i++) {
		const char c = text[i];

		// STEP 1: Grab the index of the character
		size_t index;
		if (c >= 'a' && c <= 'z') index = c - 'a';
		else if (c >= 'A' && c <= 'Z') index = c - 'A' + 26;
		else if (c >= '0' && c <= '9') index = c - '0' + 52;
		else {
			switch (c) {
			case '.':
				index = 63;
				break;
			case ':':
				index = 64;
				break;
			case ',':
				index = 65;
				break;
			case '[':
				index = 66;
				break;
			case ']':
				index = 67;
				break;
			case '(':
				index = 68;
				break;
			case ')':
				index = 69;
				break;
			case '/':
				index = 70;
				break;
			case '\\':
				index = 71;
				break;
			case '?':
				index = 72;
				break;
				// BEGIN SPECIAL CHARACTERS
			case ' ':
				dst.x += character_rects[0].w * scale;
				continue;
			case '\n':
				dst.y += (character_rects[26].h - 1) * scale * 2;
				dst.x = pos.x;
				continue;
			default:
				continue; // if we don't have a matching character, don't render.
			}
		}

		// STEP 2: Render the piece of text.
		const SDL_Rect* src = &character_rects[index];
		dst.w = src->w * scale;
		dst.h = src->h * scale;
		SDL_RenderCopy(renderer, font_texture, src, &dst);

		dst.x += (src->w + 1) * scale;
	}

}

void gord::StaticFont::setColor(const uint8_t r, const uint8_t g, const uint8_t b, const uint8_t a) {
	SDL_SetTextureColorMod(font_texture, r, g, b);
	SDL_SetTextureAlphaMod(font_texture, a);
}

void gord::StaticFont::destroy() {
	SDL_DestroyTexture(font_texture);
	font_texture = nullptr;
}


